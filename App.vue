<template>
  <div id="root">
		<div class="todo-container">
			<div class="todo-wrap">
				<!-- 想要子传父，则，必须先准备一个函数，把函数传过去 -->
				<MyHeader @addTodo="addTodo"/>
				<!-- 把App里的数据给List，父传子 -->
				<MyList :todos="todos"/>
				<MyFooter :todos="todos" @checkAllTodo="checkAllTodo" @clearDone="clearDone"/>
			</div>
		</div>
	</div>
</template>

<script>
import pubsub from 'pubsub-js';
import MyHeader from './components/MyHeader';
import MyList from './components/MyList';
import MyFooter from './components/MyFooter';
export default {
    name:'App',
    components:{
        MyHeader,MyList,MyFooter
    },
	data(){
		return {
			todos:JSON.parse(localStorage.getItem('todos')) || []
		}
	},
	methods:{
		// 添加一个todo
		addTodo(x){
			console.log('我是App组件，我收到了数据',x);
			this.todos.unshift(x);
		},
		// 勾选or取消勾选一个todo
		checkTodo(id){
			this.todos.forEach((todo)=>{
				if(todo.id===id){
					todo.done=!todo.done;
				}
			})
		},
		// 更新item内容
		updateTodo(id,title){
			this.todos.forEach((todo)=>{
				if(todo.id===id){
					todo.title=title;
				}
			})
		},
		// 删除todo
		deleteTodo(msgName,id){
			// filter不改变原数组，需要把它重新放回去
			console.log('我是消息订阅与发布中的订阅者');
			this.todos=this.todos.filter(todo=>todo.id!==id);
		},
		// 全选or取消全选
		checkAllTodo(flag){
			this.todos.forEach((todo)=>{
				todo.done=flag;
			})
		},
		// 清除所有已完成的todo
		clearDone(){
			this.todos=this.todos.filter((todo)=>{
				return !todo.done;//返回没有完成的
			})
		}
	},
	// 监视todos，进行本地存储
	watch:{
		todos:{
			deep:true,
			handler(value){
				localStorage.setItem('todos',JSON.stringify(value));
			}
		}
	},
	// 使用全局事件总线绑定自定义事件，接收数据
	mounted(){
		this.$bus.$on('checkTodo',this.checkTodo);
		this.$bus.$on('updateTodo',this.updateTodo);

		// this.$bus.$on('deleteTodo',this.deleteTodo);
		this.pubId=pubsub.subscribe('deleteTodo',this.deleteTodo);
	},
	beforeDestroy(){
		this.$bus.$off('checkTodo');
		this.$bus.$off('updateTodo');

		// this.$bus.$off('deleteTodo');
		pubsub.unsubscribe(this.pubId);
	}
	}

</script>

